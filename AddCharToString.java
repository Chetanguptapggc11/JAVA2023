public class AddCharToString {
    public static void main(String[] args) {
        // Original string
        String originalString = "Hello";

        // Character to add
        char charToAdd = '!';
        
        // Convert the character to a string and add it to the original string
        String stringWithAddedChar = originalString + charToAdd;

        // Display the result
        
        System.out.println("Original String: " + originalString);
        System.out.println("Character to Add: " + charToAdd);
        System.out.println("String with Added Character: " + stringWithAddedChar);
    }
}

